class Camera:
    def __init__(self, id, manufacturer, model, focal_length, resolution_width, resolution_height, horizontal_viewing_angle, vertical_viewing_angle, sensor_size):
        self.Id = id
        self.Manufacturer = manufacturer
        self.Model = model
        self.Focal_length = focal_length
        self.Resolution_width = resolution_width
        self.Resolution_height = resolution_height
        self.Horizontal_viewing_angle = horizontal_viewing_angle
        self.Vertical_viewing_angle = vertical_viewing_angle
        self.Sensor_size = sensor_size
